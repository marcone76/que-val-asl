package it.istruzione.ossscudig.model.dto;

public class AvvisiDTO {
	
	
	private String descrizioneAvv;
	private String descrizioneTestoAVV;
	private String codTipoAvv;
	
	public String getDescrizioneAvv() {
		return descrizioneAvv;
	}
	public void setDescrizioneAvv(String descrizioneAvv) {
		this.descrizioneAvv = descrizioneAvv;
	}
	public String getDescrizioneTestoAVV() {
		return descrizioneTestoAVV;
	}
	public void setDescrizioneTestoAVV(String descrizioneTestoAVV) {
		this.descrizioneTestoAVV = descrizioneTestoAVV;
	}
	public String getCodTipoAvv() {
		return codTipoAvv;
	}
	public void setCodTipoAvv(String codTipoAvv) {
		this.codTipoAvv = codTipoAvv;
	}
	
}
